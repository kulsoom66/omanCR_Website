const languageSelector = document.getElementById("selectLang");
languageSelector.addEventListener("change", (event) => {
	setLanguage(event.target.value);
	localStorage.setItem("lang", event.target.value);
});



const setLanguage = (language) => {
	console.log('Changing language to:', language); // Debugging

	const elements = document.querySelectorAll("[data-i18n]");
	elements.forEach((element) => {
		const translationKey = element.getAttribute("data-i18n");
		element.textContent = translations[language][translationKey];
	});

	// // Set the document direction based on the language
	document.dir = language === "ar" ? "rtl" : "ltr";

	// // Set text alignment based on document direction
	const targetElements = document.querySelectorAll("#toRight, #toRight2");
	targetElements.forEach((element) => {
		element.style.textAlign = document.dir === "rtl" ? "right" : "left";
	});

	// Change the dir attribute for specific elements if needed
	if (document.dir === "rtl") {
		// Get a reference to the element with the class name "rectangle-box"
		const rectangleBoxes = document.getElementsByClassName("rectangle-box");

		// Loop through all elements with the class name and apply styles
		for (const rectangleBox of rectangleBoxes) {
			// Set the border properties
			rectangleBox.style.border = "none"; // Remove all borders
			rectangleBox.style.borderRight = "5px solid #5554a2"; // Add a 5px solid right border with the specified color
		}

	}
};
